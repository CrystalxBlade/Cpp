#include <iostream>
#include <string>

int main() {
    int age = 20;
    float pi = 3.14f;
    double price = 99.999;
    char grade = 'A';
    bool isAlive = true;
    std::string name = "Blade";

    std::cout << "Age: " << age << std::endl;
    std::cout << "Pi: " << pi << std::endl;
    std::cout << "Price: " << price << std::endl;
    std::cout << "Grade: " << grade << std::endl;
    std::cout << "Alive: " << isAlive << std::endl;
    std::cout << "Name: " << name << std::endl;
    return 0;
}
