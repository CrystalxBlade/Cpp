#include <iostream>

int main()
{
    int a = 4, b = 2;
    std::cout<<"Addition = " << (a + b) << "\n";
    std::cout<<"Subtraction = " << (a - b) << "\n";
    std::cout<<"Product = " << (a * b) << "\n";
    std::cout<<"Division = " << (a / b) << "\n";
    std::cout<<"Modulus = " << (a % b) << "\n";
    return 0;
}